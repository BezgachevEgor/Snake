        }
        public double X2
        {
            get => x2;
            set => x2 = value;
        }
        public double Y2
        {
            get => y2;
            set => y2 = value;
        }
        public double X3
        {
            get => x3;
            set => x3 = value;
        }
        public double Y3
        {
            get => y3;
            set => y3 = value;
        }
        public double X4
        {
            get => x4;
            set => x4 = value;
        }
        public double Y4
        {
            get => y4;
            set => y4 = value;
        }      
    }
}
